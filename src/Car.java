public class Car {
    private String make;

    public String getMake() {
        return make;
    }

    public void setMake(String make) {
        this.make = make;
    }

    private String model;

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    private Number year;

    public Number getYear() {
        return year;
    }

    public void setYear(Number year) {
        this.year = year;
    }
}
